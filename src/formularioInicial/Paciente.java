    private void btnCadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadActionPerformed
        // TODO add your handling code here:
        BD acessoUm = new BD();
        String sexo = "";
        if(sexoM.isSelected()){
            sexo = "M";
        }else{
            sexo = "F";
        }
        if(Variaveis.conCad == 1){
            if(nome.getText().equals("") || data.getText().equals("")|| sexo.equals("")|| cor.getText().equals("")|| estadoCivil.getText().equals("")|| RG.getText().equals("")|| Endereco.getText().equals("")|| Cidade.getText().equals("")|| Cargo.getText().equals("")||idEmpresa.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Preencha todos os campos");
            } else {
                acessoUm.cadastrarPaciente(nome.getText(), data.getText(), sexo, cor.getText(), estadoCivil.getText(), RG.getText(), Endereco.getText(), Cidade.getText(), Cargo.getText(), pai.getText(), mae.getText(), naturalidade.getText(), Integer.parseInt(idEmpresa.getText()));
                JOptionPane.showMessageDialog(null, "Cadastrado");
                this.dispose();
                new MenuOpcoes().setVisible(true);
            }
        } else {
            if(idPaciente.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Preencha o campo de ID");
            }else{
                acessoUm.consultarPaciente(Integer.parseInt(idPaciente.getText()));
                Cargo.setText(Variaveis.Cargo);
                Cidade.setText(Variaveis.Cidade);
                Endereco.setText(Variaveis.Endereco);
                RG.setText(Variaveis.RG);
                cor.setText(Variaveis.cor);
                data.setText(Variaveis.data);
                estadoCivil.setText(Variaveis.estadoCivil);
                idEmpresa.setText(Variaveis.idEmpresa);
                nome.setText(Variaveis.nome);
                if(Variaveis.sexo.equals("M")){
                    sexoM.setSelected(true);
                    
                }else{
                    sexoF.setSelected(true);
                }
                pai.setText(Variaveis.pai);
                mae.setText(Variaveis.mae);
                naturalidade.setText(Variaveis.naturalidade);
                int aux = Funcoes.voltar("Deseja atualizar o cadastro?","SIM", "NÃO");
                if(aux == 1){
                    idPaciente.disable();
                    nome.setEnabled(true);
                    data.setEnabled(true);
                    cor.setEnabled(true);
                    estadoCivil.setEnabled(true);
                    RG.setEnabled(true);
                    Endereco.setEnabled(true);
                    Cidade.setEnabled(true);
                    Cargo.setEnabled(true);
                    idEmpresa.setEnabled(true);
                    pai.setEnabled(true);
                    mae.setEnabled(true);
                    atualizar.setVisible(true);
                    btnCad.setVisible(false);
                    naturalidade.setVisible(true);
                }
                
            }
        }
    }//GEN-LAST:event_btnCadActionPerformed

