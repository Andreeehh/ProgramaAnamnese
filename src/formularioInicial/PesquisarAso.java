    public void pesquisarAso(){
        
            Connection c = con.conectar();
            if(nome.getText().equals("")){
                JOptionPane.showMessageDialog(null,"Preencha o campo de pesquisa");
            }else{
                    try {
                        PreparedStatement pesquisarStmt = c.prepareStatement("Select t1.idAso, t2.nome, t2.RG, t1.data From aso t1 INNER JOIN paciente t2 ON t1.id_Paciente = t2.idPaciente where t2.nome LIKE ?");
                        pesquisarStmt.setString(1, "%"+ nome.getText()+"%");
                        ResultSet rs = pesquisarStmt.executeQuery();
                        
                        DefaultTableModel model;
                        model = (DefaultTableModel) tabela.getModel();
                        model.setNumRows(0);
                        if(rs == null){    
                            JOptionPane.showMessageDialog(null, "Registro n√£o encontrado");
                        }else{
                            while(rs.next()){
                                model.addRow(
                                        new Object[]{
                                            rs.getString("t1.idAso"),
                                            rs.getString("t2.nome"),
                                            rs.getString("t2.RG"),
                                            validaData(rs.getDate("t1.data")),
                                        }
                                );
                            }
                            
                        }
                    } catch (SQLException ex) {
                        Logger.getLogger(PesquisarAso.class.getName()).log(Level.SEVERE, null, ex);
                    }
}}
