    private void btnCadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadActionPerformed
        // TODO add your handling code here:
        BD acessoUm = new BD();
        if(Variaveis.conCad == 1){
            if(CNPJ.getText().equals("") || nome.getText().equals("")|| endereco.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Preencha todos os campos");
            } else {
                acessoUm.cadastrarEmpresa(CNPJ.getText(), nome.getText(), endereco.getText());
                JOptionPane.showMessageDialog(null, "Cadastrado");
                this.dispose();
                new MenuOpcoes().setVisible(true);
            }
        }else {
            if(idEmpresa.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Preencha o campo de ID");
            }else{
            acessoUm.consultarEmpresa(Integer.parseInt(idEmpresa.getText()));
            CNPJ.setText(Variaveis.CPNJ);
            nome.setText(Variaveis.NomeEmpresa);
            endereco.setText(Variaveis.EnderecoEmpresa);        
            }
        }
    }//GEN-LAST:event_btnCadActionPerformed

