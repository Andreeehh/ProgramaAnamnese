    private void idEmpresaFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_idEmpresaFocusLost
        // TODO add your handling code here:
        if(!idEmpresa.getText().equals("")){
        if(!varAux.equals(idEmpresa.getText())){
                varAux = idEmpresa.getText();
        
                BD acessoUm = new BD();
                acessoUm.consultarEmpresa(Integer.parseInt(idEmpresa.getText()));
                nome.setText(Variaveis.NomeEmpresa);
                CNPJ.setText(Variaveis.CPNJ);
                endereco.setText(Variaveis.EnderecoEmpresa);
                
                int aux = Funcoes.voltar("Deseja atualizar o cadastro?","SIM", "NÃO");
                if(aux == 1){
                    nome.setEnabled(true);
                    CNPJ.setEnabled(true);
                    endereco.setEnabled(true);
                    btnAtt.setVisible(true);
                }}}
    }//GEN-LAST:event_idEmpresaFocusLost

    private void btnAttActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAttActionPerformed
        try {
            // TODO add your handling code here:
            BD con = new BD();
            Connection c = con.conectar();
            
            PreparedStatement pesquisarStmt = c.prepareStatement("UPDATE empresa set CNPJ = ?, nome = ?, endereco = ? WHERE idEmpresa = ?");
            
            pesquisarStmt.setString(1, CNPJ.getText());
            pesquisarStmt.setString(2, nome.getText());
            pesquisarStmt.setString(3, endereco.getText());
            pesquisarStmt.setInt(4, Integer.parseInt(idEmpresa.getText()));
            pesquisarStmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Atualizado");
        } catch (SQLException ex) {
            Logger.getLogger(Empresa.class.getName()).log(Level.SEVERE, null, ex);
        }
            
    }//GEN-LAST:event_btnAttActionPerformed

