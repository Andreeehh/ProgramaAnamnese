    private void btnCadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadActionPerformed
        // TODO add your handling code here:
        int aux = 0;
        if(btnCad.getText().equals("Cadastrar")){    
            cadAnamnese();
            aux = 1;
        }
        if(btnCad.getText().equals("Atualizar")){
            attAnamnese();
            aux = 1;
        }
        if (btnCad.getText().equals("Imprimir Anamnese")){
            //int aux = Integer.parseInt(JOptionPane.showInputDialog(null,"Digite o id da Anamnese"));
        
            Connection c = con.conectar();
            
        
            String src = "Exame.jasper";
        
            JasperPrint jasperPrint = null;
            try{
                HashMap params = new HashMap<>();
            
                params.put("idAnamnese",Integer.parseInt(idAnamnese.getText()));
                jasperPrint = JasperFillManager.fillReport(src, params , c); 
            } catch (JRException ex){
                System.out.println("Error: "+ ex);
            }
        
            JasperViewer view = new JasperViewer(jasperPrint, false);
        
            view.setVisible(true);
            aux = 2;
        }
        if(aux == 1){
            btnCad.setText("Imprimir Anamnese");
        }else{
            btnCad.setText("Cadastrar");
        }
    }//GEN-LAST:event_btnCadActionPerformed

