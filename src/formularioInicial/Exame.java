    public void idAnamnese(){
        Connection c = con.conectar();
        try{    
            String apto = "";
            if(aptoRadioSim.isSelected()){
                apto = "APTO";
            }else{
                apto = "INAPTO";
            }
            PreparedStatement pesquisarStmt = c.prepareStatement("SELECT * FROM exame WHERE id_Paciente = ? AND tipoExame = ? AND data = ? AND apto = ?");
            pesquisarStmt.setInt(1, Integer.parseInt(idPaciente.getText()));
            pesquisarStmt.setString(2, tipoExame.getSelectedItem().toString());
            pesquisarStmt.setString(3, dataExame.getText());
            pesquisarStmt.setString(4, apto);
            ResultSet rs = pesquisarStmt.executeQuery();
            while(rs.next()){
                idAnamnese.setText(rs.getString("idAnamnese"));
            }
            
            
        }catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Erro ao pesquisar");
            System.out.println(e);
            //System.out.println("Ocorreu um erro ao conectar");
        }
    }
